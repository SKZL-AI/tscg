/**
 * TAB LaTeX Reporter
 *
 * Generates LaTeX tables from benchmark results for inclusion in the TSCG paper.
 * Produces publication-ready tables with booktabs formatting.
 */

import { writeFileSync, mkdirSync } from 'node:fs';
import { join } from 'node:path';
import type { BenchmarkReport, AggregateMetrics, Condition } from '../types.js';

export interface LatexReporterOptions {
  /** Output directory for LaTeX files */
  outputDir: string;
  /** Custom filename prefix (default: "tab") */
  prefix?: string;
  /** Caption prefix for tables */
  captionPrefix?: string;
}

/**
 * Generate LaTeX tables from benchmark results.
 *
 * @returns Path to the saved .tex file
 */
export function saveLatexReport(
  report: BenchmarkReport,
  options: LatexReporterOptions,
): string {
  mkdirSync(options.outputDir, { recursive: true });

  const prefix = options.prefix ?? 'tab';
  const filepath = join(options.outputDir, `${prefix}-tables.tex`);
  const captionPrefix = options.captionPrefix ?? 'TAB';

  const sections: string[] = [
    `% Auto-generated by TAB Benchmark Harness`,
    `% Scenario: ${report.meta.scenario}`,
    `% Generated: ${report.meta.end_time}`,
    `% Models: ${report.meta.models.join(', ')}`,
    '',
  ];

  // Table 1: Main results
  sections.push(generateMainResultsTable(report.aggregates, report.meta.scenario, captionPrefix));

  // Table 2: TSCG vs Natural comparison
  const tscgAggs = report.aggregates.filter(a => a.condition !== 'natural');
  if (tscgAggs.length > 0) {
    sections.push(generateComparisonTable(tscgAggs, report.meta.scenario, captionPrefix));
  }

  // Table 3: Token/cost savings
  if (tscgAggs.length > 0) {
    sections.push(generateSavingsTable(tscgAggs, report.meta.scenario, captionPrefix));
  }

  const content = sections.join('\n\n');
  writeFileSync(filepath, content, 'utf-8');

  console.log(`  [LaTeX] Tables saved: ${filepath}`);
  return filepath;
}

/**
 * Generate the main results table.
 */
function generateMainResultsTable(
  aggregates: AggregateMetrics[],
  scenario: string,
  captionPrefix: string,
): string {
  const lines: string[] = [
    `\\begin{table}[htbp]`,
    `  \\centering`,
    `  \\caption{${captionPrefix} Scenario ${scenario}: Main Results}`,
    `  \\label{tab:${scenario.toLowerCase()}-main}`,
    `  \\begin{tabular}{llcccr}`,
    `    \\toprule`,
    `    Model & Condition & Accuracy & Tool Sel. & Param F1 & $n$ \\\\`,
    `    \\midrule`,
  ];

  // Group by model for visual separation
  let lastModel = '';
  for (const a of aggregates) {
    if (lastModel && a.model !== lastModel) {
      lines.push(`    \\addlinespace`);
    }
    lastModel = a.model;

    const acc = `${(a.accuracy.mean * 100).toFixed(1)}\\%`;
    const ci = `\\tiny{[${(a.accuracy.ci95[0] * 100).toFixed(1)}--${(a.accuracy.ci95[1] * 100).toFixed(1)}]}`;
    const toolSel = `${(a.tool_selection_accuracy.mean * 100).toFixed(1)}\\%`;
    const paramF1 = `${(a.parameter_f1.mean * 100).toFixed(1)}\\%`;

    // Bold the best condition per model
    const condDisplay = formatCondition(a.condition);

    lines.push(
      `    ${escapeLatex(a.model)} & ${condDisplay} & ${acc} ${ci} & ${toolSel} & ${paramF1} & ${a.n_tasks} \\\\`,
    );
  }

  lines.push(
    `    \\bottomrule`,
    `  \\end{tabular}`,
    `\\end{table}`,
  );

  return lines.join('\n');
}

/**
 * Generate TSCG vs Natural comparison table with ARR.
 */
function generateComparisonTable(
  tscgAggregates: AggregateMetrics[],
  scenario: string,
  captionPrefix: string,
): string {
  const lines: string[] = [
    `\\begin{table}[htbp]`,
    `  \\centering`,
    `  \\caption{${captionPrefix} Scenario ${scenario}: Accuracy Retention Rate (ARR)}`,
    `  \\label{tab:${scenario.toLowerCase()}-arr}`,
    `  \\begin{tabular}{llccc}`,
    `    \\toprule`,
    `    Model & Condition & ARR & Acc. (TSCG) & Acc. (Natural) \\\\`,
    `    \\midrule`,
  ];

  for (const a of tscgAggregates) {
    const arr = a.arr === 0 ? '---' : `${(a.arr * 100).toFixed(1)}\\%`;
    const accTscg = `${(a.accuracy.mean * 100).toFixed(1)}\\%`;
    const condDisplay = formatCondition(a.condition);

    lines.push(
      `    ${escapeLatex(a.model)} & ${condDisplay} & ${arr} & ${accTscg} & --- \\\\`,
    );
  }

  lines.push(
    `    \\bottomrule`,
    `  \\end{tabular}`,
    `\\end{table}`,
  );

  return lines.join('\n');
}

/**
 * Generate token and cost savings table.
 */
function generateSavingsTable(
  tscgAggregates: AggregateMetrics[],
  scenario: string,
  captionPrefix: string,
): string {
  const lines: string[] = [
    `\\begin{table}[htbp]`,
    `  \\centering`,
    `  \\caption{${captionPrefix} Scenario ${scenario}: Token and Cost Savings}`,
    `  \\label{tab:${scenario.toLowerCase()}-savings}`,
    `  \\begin{tabular}{llcc}`,
    `    \\toprule`,
    `    Model & Condition & Token Savings & Cost Savings \\\\`,
    `    \\midrule`,
  ];

  for (const a of tscgAggregates) {
    const tokSave = `${a.token_savings_pct.toFixed(1)}\\%`;
    const costSave = `${a.cost_savings_pct.toFixed(1)}\\%`;
    const condDisplay = formatCondition(a.condition);

    lines.push(
      `    ${escapeLatex(a.model)} & ${condDisplay} & ${tokSave} & ${costSave} \\\\`,
    );
  }

  lines.push(
    `    \\bottomrule`,
    `  \\end{tabular}`,
    `\\end{table}`,
  );

  return lines.join('\n');
}

/**
 * Format condition name for LaTeX display.
 */
function formatCondition(condition: Condition): string {
  switch (condition) {
    case 'natural':
      return 'Natural';
    case 'tscg':
      return 'TSCG';
    case 'tscg_sad':
      return 'TSCG+SAD';
    default:
      return condition;
  }
}

/**
 * Escape special LaTeX characters in a string.
 */
function escapeLatex(text: string): string {
  return text
    .replace(/\\/g, '\\textbackslash{}')
    .replace(/[&%$#_{}]/g, match => '\\' + match)
    .replace(/~/g, '\\textasciitilde{}')
    .replace(/\^/g, '\\textasciicircum{}');
}
